    private void getHarga() {
        String merk = (String) lsMerk.getSelectedValue();

        if (merk != null) {
            if (merk.equals("Aqua")) {
                if (rbGelas.isSelected()) {
                    tfHarga.setText("500");
                } else {
                    tfHarga.setText("3000");
                }
            } else {
                if (rbGelas.isSelected()) {
                    tfHarga.setText("450");
                } else {
                    tfHarga.setText("2800");
                }
            }
        } 
    }

